# Module math
import math

# Trigonometrische Funktionen
x = 45    # grad
xbm = math.radians(x)

print("Sinus   ", x, "Grad:", math.sin(xbm))
print("Cosinus ", x, "Grad:", math.cos(xbm))
print("Tangens ", x, "Grad:", math.tan(xbm))



